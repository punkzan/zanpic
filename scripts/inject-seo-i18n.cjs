// Process each locale file
for (const [lang, seoStrings] of Object.entries(translations)) {
  const filePath = path.join(LOCALE_DIR, `${lang}.json`)
  const json = JSON.parse(fs.readFileSync(filePath, 'utf8'))

  // Ensure admin.settings exists
  if (!json.admin) json.admin = {}
  if (!json.admin.settings) json.admin.settings = {}

  // Inject seo* keys
  for (const [key, value] of Object.entries(seoStrings)) {
    json.admin.settings[key] = value
  }

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8')
  console.log(`✓ ${lang}.json — added ${Object.keys(seoStrings).length} SEO keys`)
}

console.log('\nDone! All 8 locale files updated.')
